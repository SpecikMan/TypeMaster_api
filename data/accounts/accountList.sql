SELECT [idAccount]
      ,[username]
      ,[password]
      ,[email]
      ,(SELECT FORMAT ([createDate], 'yyyy-MM-dd')) as [createDate]
      ,(SELECT FORMAT ([latestLoginDate], 'yyyy-MM-dd')) as [latestLoginDate]
      ,[countLoginDate]
      ,[pathImage]
      ,[fullName]
      ,(SELECT FORMAT ([dob], 'yyyy-MM-dd')) as [dob]
      ,[gender]
      ,[coin]
      ,[verificationCode]
      ,[uud]
      ,[accountLevel]
      ,[levelCap]
      ,[levelExp]
      ,[Account].[idRole]
      ,[nameRole]
      ,Account.[idRank]
      ,[rankName]
      ,[reward]
      ,Rank.[imagePath]
      ,[promote]
      ,[demote]
FROM [dbo].[Account],[dbo].[Role],[dbo].[Rank]
WHERE [Account].[idRole] = [Role].[idRole] and [Rank].[idRank] = [Account].[idRank]