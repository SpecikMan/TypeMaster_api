SELECT 
      [Account].[username],
      [Account].[password],
      [Account].[email],
      [Account].[fullName],
      (SELECT FORMAT ([Account].[dob], 'yyyy-MM-dd')) as [dob],
      [Account].[verificationCode],
      [Account].[uud],
      [Account].[accountLevel],
      [Account].[levelCap],
      [Account].[levelExp],
      [Account].[idRole],
      [AccountLevelDetails].[idLevelDetails],
      [AccountLevelDetails].[idAccount],
      [AccountLevelDetails].[idLevel],
      [AccountLevelDetails].[score],
      [AccountLevelDetails].[timeLeft],
      (SELECT FORMAT ([AccountLevelDetails].[datePlayed], 'yyyy-MM-dd')) as [datePlayed],
      [AccountLevelDetails].[isLike],
      [AccountLevelDetails].[wpm],
      [AccountLevelDetails].[wps],
      [AccountLevelDetails].[correct],
      [AccountLevelDetails].[wrong],
      [AccountLevelDetails].[accuracy],
      [AccountLevelDetails].[chartData],
      [Level].[nameLevel],
      [Level].[numLike],
      (SELECT FORMAT ([Level].[createDate], 'yyyy-MM-dd')) as [createDate],
      (SELECT FORMAT ([Level].[updatedDate], 'yyyy-MM-dd')) as [updatedDate],
      [Level].[levelContent],
      [Level].[totalWords],
      [Level].[time],
      [Level].[idDifficulty],
      [Level].[idMode],
      [Level].[idPublisher]
FROM [dbo].[AccountLevelDetails],[dbo].[Level],[dbo].[Account]
WHERE [AccountLevelDetails].[idLevel] = [Level].[idLevel] and [AccountLevelDetails].[idAccount] = [Account].[idAccount] and [AccountLevelDetails].[idLevelDetails] = @idLevelDetails